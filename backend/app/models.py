        # –°–æ–∑–¥–∞—ë–º —Ä–æ–ª–∏
        for role_name, role_data in cls.DEFAULT_ROLES.items():
            role = Role(name=role_name, description=role_data["description"])
            role.permissions = [permissions_by_name[p] for p in role_data["permissions"]]
            session.add(role)

        await session.commit()
